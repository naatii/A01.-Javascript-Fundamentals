const string = "String"
const number = 1
const boolean = true

console.log(`${string}: ${typeof(string)}`)
console.log(`${number}: ${typeof(number)}`)
console.log(`${boolean}: ${typeof(boolean)}`)

console.log(`${string}: ${typeof(string)}`)
